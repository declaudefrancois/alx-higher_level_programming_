#!/usr/bin/python3
def add(a, b):
    """Adds two integers

    Args:
        a: first integer
        b: second integer

    Returns:
        The sesult of a + b
    """
    return (a + b)


def sub(a, b):
    """Substracts the second integer from the first.

    Args:
        a: first integer
        b: second integer

    Returns:
        The sesult of a - b
    """
    return (a - b)


def mul(a, b):
    """Multiply two integers.

    Args:
        a: first integer
        b: second integer

    Returns:
        The sesult of a * b
    """
    return (a * b)


def div(a, b):
    """Substracts the second integer from the first.

    Args:
        a: first integer
        b: second integer

    Returns:
        The sesult of a / b
    """
    return (a / b)
